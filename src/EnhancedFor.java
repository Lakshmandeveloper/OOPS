
public class EnhancedFor {

	public static void main(String[] args) {
		 int a[]= {1,2,3,4,5,6};
		 for(int i:a)
			 System.out.print(i);
		 System.out.println();
		 float b[]= {4.5f,5.5f,6.5f,7.5f};
			for(float i:b)
				System.out.print(i);
			System.out.println();
			
			String s[]= {"abcde","xyz","mno"};
			for(String i:s)
				System.out.print(i);
			System.out.println();

	}

}
